    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (txtCodigo.getText().isEmpty()){

            JOptionPane.showMessageDialog(null, "Selecione o código do Funcionário!");
        } else {
            funcionario = new Funcionario();

            funcionario.setCodigoFuncionario(Integer.parseInt(txtCodigo.getText()));
            int confirma = JOptionPane.showConfirmDialog(null,"Deseja realmente Excluir?"+ txtNome.getText());
            if (confirma == 0){

                limpaCamposFuncionario();
                atualizarTabelaFuncionario();

            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNome.getText().isEmpty() || txtRua.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            txtNome.requestFocusInWindow();
        } else{

            funcionario = new Funcionario();
            funcionario.setNomeFuncionario(txtNome.getText());
            funcionario.setRuaFuncionario(txtRua.getText());
            funcionario.setNumeroFuncionario(txtNumero.getText());
            funcionario.setComplementoFuncionario(txtComplemento.getText());
            funcionario.setBairroFuncionario(txtBairro.getText());
            funcionario.setRgFuncionario(txtRg.getText());
            funcionario.setCpfFuncionario(txtCpf.getText());
            funcionario.setSexoFuncionario(txtSexo.getText());
            funcionario.setTelefoneFuncionario(txtTelefone.getText());
            funcionario.setCidadeFuncionario(txtCidade.getText());
            funcionario.setEstadoFuncionario(txtEstado.getText());
            funcionario.setCepFuncionario(txtCep.getText());
            funcionario.setEmailFuncionario(txtEmail.getText());
            funcionario.setEmailFuncionario(txtSalario.getText());
            funcionario.setEmailFuncionario(txtFuncao.getText());

            limpaCamposFuncionario();
            atualizarTabelaFuncionario();

        }
    }//GEN-LAST:event_btnSalvarActionPerformed

